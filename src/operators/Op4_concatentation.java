package operators;

public class Op4_concatentation {
    public static void main(String[] args) {
        String s1= "Hello ";
        String s2= s1 + "World";
        System.out.println(s2);

        int a = 5;
        int b = 10;
        b+=a;
        System.out.println(b);
        b-=a;
        System.out.println(b);
        b*=a;
        System.out.println(b);
        b/=a;
        System.out.println(b);
        b%=a;
        System.out.println(b);


    }
}
